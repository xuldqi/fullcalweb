    updateLanguageSelector() {
        // Just sync the UI dropdown value
        const selector = document.getElementById('language-selector');
        if (selector) {
            selector.value = this.currentLanguage;
        }
    }

    bindEvents() {
        const selector = document.getElementById('language-selector');
        if (selector) {
            selector.addEventListener('change', (e) => {
                const newLang = e.target.value;
                this.setLanguage(newLang);
                // Save preference ONLY when user manually changes it
                localStorage.setItem('fullcal_language', newLang);
                console.log('User manually set language to:', newLang);
            });
        }
    }

